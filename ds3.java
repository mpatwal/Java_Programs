public class ds3 {

    public static void main(String[] args) {
    
    
        int arr[]={23,67,45,78,12,16,90};
        int n =55;
        
        for(int i=0;i<arr[i];i++)
        {
            if (arr[i]==n)
            {
              System.out.println("the element is found at index "+i);  
              
            }
            else if (arr[i]!=n)
            {
              System.out.print(-1);  
              
            }
            else
            {
              System.out.println(" not found");
            }
            
        }

             
    }
}